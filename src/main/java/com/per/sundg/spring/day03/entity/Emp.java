package com.per.sundg.spring.day03.entity;

public class Emp {
	private Integer id;
	private String name;
	public void setId(Integer id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getId() {
		return id;
	}
	public String getName() {
		return name;
	}
}
